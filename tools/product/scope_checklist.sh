#!/bin/bash
# scope_checklist.sh - MVP スコープチェックリスト生成
# 使い方: ./tools/product/scope_checklist.sh "プロジェクト名"
set -euo pipefail

PROJECT="${1:-プロジェクト名}"

cat <<EOF
# ${PROJECT} MVP スコープチェックリスト

## スコープ判定チェック

各機能について以下の質問に答えてください:

### 機能: （機能名を記入）

| # | 質問 | 回答 |
|---|---|---|
| 1 | この機能がないとユーザーの課題は解決できない？ | Yes / No |
| 2 | リリース後に追加しても問題ない？ | Yes / No |
| 3 | この機能の実装に必要な工数は？ | 小/中/大 |
| 4 | この機能がないと他の MVP 機能が動かない？ | Yes / No |
| 5 | 競合と比較して初日から必要？ | Yes / No |

**判定**: Q1=Yes かつ Q2=No → MVP 必須

## MVP 機能一覧
| 機能 | 必須理由 | 受入条件数 | 工数見積 |
|---|---|---|---|
| | | | |

## MVP 外（Phase 2 以降）
| 機能 | 見送り理由 | 予定 Phase |
|---|---|---|
| | | |

## スコープクリープ防止チェック
- [ ] MVP の機能数は 5-10 個に収まっているか
- [ ] 各機能の受入条件は明確か
- [ ] 「あったら良い」と「なければダメ」を区別したか
- [ ] ステークホルダーと合意したか
- [ ] 技術的に最小限の実装で実現できるか

---
MVP スコープチェックリスト生成完了: ${PROJECT}
EOF
exit 0
