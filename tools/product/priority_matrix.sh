#!/bin/bash
# priority_matrix.sh - 優先度マトリクステンプレート生成
# 使い方: ./tools/product/priority_matrix.sh
set -euo pipefail

cat <<'EOF'
# 優先度マトリクス

## マトリクス（影響度 × 工数）

|  | 工数: 小 (< 1日) | 工数: 中 (1-5日) | 工数: 大 (> 5日) |
|---|---|---|---|
| **影響度: 高** | P0: 即実装 | P0: 優先実装 | P1: 要検討 |
| **影響度: 中** | P1: 推奨 | P1: 計画的に | P2: 後回し |
| **影響度: 低** | P2: 余裕時 | P2: 見送り候補 | P3: 見送り |

## 機能別評価

| # | 機能名 | 影響度 (1-5) | 工数 (1-5) | スコア | 判定 | 備考 |
|---|---|---|---|---|---|---|
| 1 | | /5 | /5 | | P?  | |
| 2 | | /5 | /5 | | P?  | |
| 3 | | /5 | /5 | | P?  | |
| 4 | | /5 | /5 | | P?  | |
| 5 | | /5 | /5 | | P?  | |

## スコア計算方法
- スコア = 影響度 × (6 - 工数)
- スコア高い順に優先度を付与

## 優先度の定義
| 優先度 | 意味 | アクション |
|---|---|---|
| P0 | MVP 必須 | 最初のリリースに含める |
| P1 | 重要 | MVP 直後に実装 |
| P2 | あると良い | 余裕があれば実装 |
| P3 | 将来検討 | バックログに積む |

---
優先度マトリクステンプレート生成完了
EOF
exit 0
