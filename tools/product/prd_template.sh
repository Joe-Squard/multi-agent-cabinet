#!/bin/bash
# prd_template.sh - PRD (Product Requirements Document) テンプレート生成
# 使い方: ./tools/product/prd_template.sh "プロジェクト名"
set -euo pipefail

PROJECT="${1:-プロジェクト名}"

cat <<EOF
# ${PROJECT} PRD (Product Requirements Document)

## 概要
- **目的**: （なぜ作るのか）
- **ターゲットユーザー**: （誰のためか）
- **解決する課題**: （何が困っているか）
- **提供する価値**: （どう解決するか）

## ペルソナ
### ペルソナ 1: （名前）
- 属性: （年齢、職業、技術レベル等）
- 課題: （日常的に抱えている問題）
- ゴール: （達成したいこと）
- 行動パターン: （普段の行動や習慣）

### ペルソナ 2: （名前）
- 属性:
- 課題:
- ゴール:
- 行動パターン:

## 機能要件
### F-001: （機能名）
- ユーザーストーリー: 「（誰）として、（何を）したい。なぜなら（理由）」
- 受入条件:
  - [ ] ...
  - [ ] ...
- 優先度: P0 (MVP)

### F-002: （機能名）
- ユーザーストーリー:
- 受入条件:
  - [ ] ...
- 優先度: P1

## スコープ
### In Scope (MVP)
- ...

### Out of Scope
- ...

### 将来検討
- ...

## 成功指標 (KPI)
| 指標 | 目標値 | 計測方法 |
|---|---|---|
| ... | ... | ... |

## リスク・前提条件
| リスク | 影響度 | 対策 |
|---|---|---|
| ... | 高/中/低 | ... |

## タイムライン
| フェーズ | 期間 | 成果物 |
|---|---|---|
| Phase 1 (MVP) | | |
| Phase 2 | | |

---
PRD テンプレート生成完了: ${PROJECT}
EOF
exit 0
